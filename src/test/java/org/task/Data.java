package org.task;

import java.util.ArrayList;

public class Data {
	private int page;
	private int per_page;
	private int total;
	private int total_pages;
	private ArrayList<Data>Details;
	public int getPage() {
		return page;
	}
	public int getPer_page() {
		return per_page;
	}
	public int getTotal() {
		return total;
	}
	public int getTotal_pages() {
		return total_pages;
	}
	public ArrayList<Data> getDetails() {
		return Details;
	}
	

}
